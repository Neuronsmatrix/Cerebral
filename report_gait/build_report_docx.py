para("alignment.py — регистрация и временная синхронизация. estimate_rigid_transform реализует метод "
     "Umeyama (1991) с отражательно-свободным SVD и равномерным масштабом (dst ≈ s·(src·RT) + T); его "
     "результаты совпадают с VvsC.compute_similarity_transform до 1e-9 (тест паритета, критерий №1). "
     "estimate_time_shift_xcorr выравнивает временные шкалы по кросс-корреляции 3D-скорости таза; "
     "detect_sync_event — вспомогательный детектор события-синхронизации.")
