h("5.2. Модуль kinematics", 2)
para("Фильтрация (filters.py). fill_gaps(df, max_gap_frames=10) кубически интерполирует внутренние "
     "пропуски не длиннее заданного. butterworth_filter — низкочастотный нуль-фазовый фильтр (filtfilt):")
mono("nyq = 0.5 * fs\n"
     "if cutoff_hz >= nyq:\n"
     "    raise ValueError(...)\n"
     "wn = cutoff_hz / nyq\n"
     "b, a = butter(order, wn, btype=\"low\")\n"
     "return filtfilt(b, a, signal, padtype=\"odd\")   # нуль-фазовый, без сдвига")
para("События походки (gait_events.py). По экстремумам вертикальной траектории стопы (find_peaks): "
     "контакт пятки = минимум высоты пятки, отрыв носка = максимум высоты носка (foot_index):")
mono("min_dist = max(1, int(min_stride_sec * fps))\n"
     "hs, _ = find_peaks(-heel_v, distance=min_dist)   # контакт пятки\n"
     "to, _ = find_peaks(toe_v,   distance=min_dist)    # отрыв носка")
para("Суставные углы (joint_angles.py). Угол между двумя векторами из вершины-сустава (0–180°). Тройки: "
     "тазобедренный (плечо–таз–колено), коленный (таз–колено–голеностоп), голеностопный "
     "(колено–голеностоп–носок):")
mono("v1 = p1 - vertex\n"
     "v2 = p2 - vertex\n"
     "cos = np.clip(np.dot(v1, v2) / (n1 * n2), -1.0, 1.0)\n"
     "return float(np.degrees(np.arccos(cos)))")
para("На текущем этапе сохраняется «включённый» угол 0–180°; перевод в клинический знак сгиба/разгиба и "
     "тазовые углы отнесены к доработкам (см. §8).", italic=True)
para("Нормирование цикла (normalizer.py). Сигнал делится на циклы HS→HS, каждый интерполируется к 101 "
     "точке (0–100 %), затем берётся NaN-устойчивое «среднее ± СКО».")
para("Пространственно-временные параметры (spatiotemporal.py). Темп = (число шагов / длительность) · 60; "
     "направление движения — методом главных компонент (SVD) в горизонтальной плоскости; длина цикла — "
     "расстояние между последовательными контактами пятки с отбраковкой нефизиологичных значений; "
     "скорость = длина цикла · темп / 120.")
mono("strides = [d for d in strides_all if d <= max_stride_m]   # физиологический потолок\n"
     "stride_length = float(np.mean(strides)) if strides else np.nan\n"
     "speed = stride_length * cadence / 120.0")
