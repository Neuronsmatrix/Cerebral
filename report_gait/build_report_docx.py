def h(text, level):
    return doc.add_heading(text, level=level)


def para(text="", bold=False, italic=False, size=12, center=False):
    p = doc.add_paragraph()
    r = p.add_run(text)
    r.bold, r.italic = bold, italic
    r.font.size = Pt(size)
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER if center else WD_ALIGN_PARAGRAPH.JUSTIFY
    return p


def note(label, text):
    p = doc.add_paragraph()
    p.paragraph_format.left_indent = Pt(12)
    rl = p.add_run(label)
    rl.bold = True
    p.add_run(" " + text)
    p.alignment = WD_ALIGN_PARAGRAPH.JUSTIFY
    return p


def bullet(text, lead=None):
    p = doc.add_paragraph(style="List Bullet")
    if lead:
        p.add_run(lead).bold = True
    p.add_run(text)
    return p


def numbered(text, lead=None):
    p = doc.add_paragraph(style="List Number")
    if lead:
        p.add_run(lead).bold = True
    p.add_run(text)
    return p


def mono(text):
    p = doc.add_paragraph()
    p.paragraph_format.space_after = Pt(6)
    lines = text.split("\n")
    for i, line in enumerate(lines):
        r = p.add_run(line if line != "" else " ")
        r.font.name = "Courier New"
        r.font.size = Pt(8.5)
        if i != len(lines) - 1:
            r.add_break()
    return p


def table(headers, rows, sizes=10):
    t = doc.add_table(rows=1, cols=len(headers))
    t.style = "Table Grid"
    for i, head in enumerate(headers):
        c = t.rows[0].cells[i]
        c.text = ""
        rr = c.paragraphs[0].add_run(head)
        rr.bold = True
        rr.font.size = Pt(sizes)
    for row in rows:
        cells = t.add_row().cells
        for i, val in enumerate(row):
            cells[i].text = ""
            rr = cells[i].paragraphs[0].add_run(str(val))
            rr.font.size = Pt(sizes)
    return t


def figure(fname, width_in, caption):
    path = FIG / fname
    if path.exists():
        p = doc.add_paragraph()
        p.alignment = WD_ALIGN_PARAGRAPH.CENTER
        p.add_run().add_picture(str(path), width=Inches(width_in))
    cap = doc.add_paragraph()
    cap.alignment = WD_ALIGN_PARAGRAPH.CENTER
    r = cap.add_run(caption)
    r.italic = True
    r.font.size = Pt(9.5)
    return cap


